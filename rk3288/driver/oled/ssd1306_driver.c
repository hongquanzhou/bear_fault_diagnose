static int ssd1306_module_init(void)
{
    printk("driver enter\n");
    int result;
    i2c_add_driver(&ssd1306_driver);

    result = alloc_chrdev_region(&ndev, 0, 1, "chr_dev");
    if(result < 0 )
    {
          return result;
    }
    printk("chr_init(): major = %d, minor = %d\n", MAJOR(ndev), MINOR(ndev));
    chr_major = MAJOR(ndev);
    char_devp = vmalloc(sizeof(struct chr_dev));
    if(!char_devp)
    {
            result = -ENOMEM;
    }
    memset(char_devp, 0, sizeof(struct chr_dev));
    chr_setup_cdev(char_devp, 0);
    ssd1306_char_class = class_create(THIS_MODULE,"ssd1306_class");
    ssd1306_char_device = device_create(ssd1306_char_class,NULL,ndev,NULL,"ssd1306_oled");
    return 0;
    pr_debug("i2c-core: driver registered\n");
    return 0;
}

static void ssd1306_module_exit(void)
{
    device_destroy(ssd1306_char_class,ndev);
    class_destroy(ssd1306_char_class);
    cdev_del(&char_devp->cdev);
    vfree(char_devp);
    unregister_chrdev_region(MKDEV(chr_major, 0), 1);
    printk("ssd1306 driver exit\n");
    ssd1306_display_off();
    i2c_del_driver(&ssd1306_driver);
}

